#include "rush01.h"

void	ft_putnbr(int nb)
{
	long	n;

	n = nb;
	if (n < 0)
	{
		n = -n;
	}
	if (n < 10)
	{
		ft_putchar(n - '0');
		return ;
	}
	ft_putnbr(n / 10);
	ft_putnbr(n % 10);
}
