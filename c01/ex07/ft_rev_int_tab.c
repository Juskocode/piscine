void	ft_rev_int_tab(int *tab, int size)
{
	int	l;
	int	r;
	int	swap;

	l = 0;
	r = size - 1;
	while (l < r)
	{
		swap = tab[l];
		tab[l] = tab[r];
		tab[r] = swap;
		l++;
		r--;
	}
}
